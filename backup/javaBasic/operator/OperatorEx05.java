public class OperatorEx05{
    public static void main(String[] args){
        // 대입(할당)연산자 : =
        // 복합대입연산자
        // 산술연산자 + 대입연산자
        
        int a = 10;
        a = a + 10;
        System.out.println(a);

        // 복합대입연산자 => 산술연산자 + 대입연산자
        a += 10;
        System.out.println(a);
    }
}